export function formatPrice(price) {
  return price.toLocaleString('fr-FR') + ' FCFA'
}

export function getProductById(id) {
  return products.find(p => p.id === id) || null
}

export function getShopById(id) {
  return shops.find(s => s.id === id) || null
}

export function getProductsByShop(shopId) {
  return products.filter(p => p.shopId === shopId)
}

export function getProductsByCategory(categoryId) {
  return products.filter(p => p.categoryId === categoryId)
}

export function getShopsByCategory(categoryId) {
  return shops.filter(s => s.categoryId === categoryId)
}

export function getStatusColor(status) {
  const colors = {
    active: '#22c55e', draft: '#6b7280', expired: '#ef4444', paused: '#f59e0b',
  }
  return colors[status] || '#6b7280'
}
